package tasc.calculator.console;

public class Operation {
    public static int aPlus(int number_first,int number_second){
        return number_first + number_second;
    }
    public static int aMinus(int number_first,int number_second){
        return number_first - number_second;
    }
    public static int aMultiply(int number_first,int number_second){
        return number_first * number_second;
    }
    public static int aDivision(int number_first,int number_second){
        return number_first / number_second;
    }
}
